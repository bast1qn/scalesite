/**
 * Dynamically load an icon by name
 * Use this for user-configurable icons only
 *
 * @example
 * const Icon = await getIcon('home');
 * return <Icon />;
 */
export async function getIcon(name: string) {
    try {
        const icon = await import(`lucide-react/dist/esm/icons/${name}`);
        return icon.default;
    } catch (error) {
        console.warn(`Icon "${name}" not found`);
        return null;
    }
}
